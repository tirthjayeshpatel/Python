#String Slicing= Allows to access some parts of string.
name="Madhav"
print(name[0:2])
print(name[0:5:2])
print(name[0::2])
print(name[0:1])
print(name[:1])
print(name[2:5])
print(name[5:])
print(name[-1:])
print(name[4:])
print(name[-2:])
print(name[1:-1])
print(name[:])
print(name[::])
print(name[::-1]) #reverse string